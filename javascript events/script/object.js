
// Create a JSON object and access it using dot notation. 

// Creating a JSON object
const person = {
    name: "Alice",
    age: 30,
    city: "New York",
    hobbies: ["reading", "traveling", "cooking"]
};

// Accessing properties using dot notation
console.log(person.name);  // Output: Alice
console.log(person.age);   // Output: 30
console.log(person.city);  // Output: New York
console.log(person.hobbies); // Output: ["reading", "traveling", "cooking"]

// Accessing an element from the hobbies array
console.log(person.hobbies[1]); // Output: traveling
